function getHtml(fabric, fabricIndex) {
  return '<div class="card col-md-3 col-11 card-click-action" data-fabric-index="' + fabricIndex + '">' +
    '<img class="card-img-top fabricImage" src="' + fabric.image + '">' +
    '<h2 class="card-title text-center">' + fabric.name + '</h2>' +
    '<h3 class="card-title text-center">$' + fabric.price + '</h3>' +
    '</div>';
}

function getModalHtml(fabric) {
  return '<div class="modal-content">' +
      '<div class="modal-header">' +
        '<h5 class="modal-title">' + fabric.name + '</h5>' +
        '<button type="button" class="close" data-dismiss="modal" aria-label="Close" id="modal-close">' +
        '<span aria-hidden="true">&times;</span>' +
        '</button>' +
      '</div>' +
      '<div class="modal-body">' +
        '<img class="card-img-top fabricImage" src="' + fabric.image + '">' +
        '<h3 class="card-title text-center">$' + fabric.price + '</h3>' +
        '<p class="card-text">Fabric Type: <strong>' + fabric.type + '</strong></p>' +
        '<p class="card-text"><strong>' + fabric.size + 'cm</strong></p>' +
        '<p class="card-text">' + fabric.description + '</p>' +
      '</div>' +
    '</div>';
}

