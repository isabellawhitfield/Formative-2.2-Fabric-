function addCardClickListeners() {
  $('.card-click-action').click(function () {

    var fabricIndex = parseInt($(this).attr('data-fabric-index'));
    $('#modal-wrapper').show();
    document.getElementById('modal-content').innerHTML = getModalHtml(fabric[fabricIndex]);

    $('#modal-close').click(function () {
      $('#modal-wrapper').hide();
    });
  });
}
